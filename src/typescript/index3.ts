interface Lengthwise {
    length: number;
}

/** T extends xxx表示 泛型T的类型被xxx约束了 */
function loggingIdentity<T extends Lengthwise>(arg: T): T {
    console.log(arg.length);  // Now we know it has a .length property, so no more error
    return arg;
}

const b = [1, 2, 3];
loggingIdentity(b);


